/* === I18n Class === */
class I18n {
  constructor() {
    this.locale = localStorage.getItem('ob_locale') || 'en';
  }

  t(key) {
    const lang = TRANSLATIONS[this.locale] || TRANSLATIONS.en;
    return lang[key] || TRANSLATIONS.en[key] || key;
  }

  setLocale(locale) {
    if (TRANSLATIONS[locale]) {
      this.locale = locale;
      localStorage.setItem('ob_locale', locale);
    }
  }

  getLocale() {
    return this.locale;
  }

  translatePage() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      const text = this.t(key);
      if (el.tagName === 'INPUT' && el.type !== 'submit') {
        el.placeholder = text;
      } else {
        el.textContent = text;
      }
    });
  }
}

window.I18n = I18n;
